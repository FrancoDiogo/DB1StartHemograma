package br.com.db1;

public class SerieVermelha extends Hemograma {

	private Double eritrocitos;

	private Double hemoglobina;

	private Double hematocrito;

	private Double vcm;

	private Double hcm;

	private Double chcm;

	private Double rdwSd;

	private Double rdwCv;

	public Double getEritrocitos() {
		return eritrocitos;
	}

	public void setEritrocitos(Double eritrocitos) {
		this.eritrocitos = eritrocitos;
	}

	public Double getHemoglobina() {
		return hemoglobina;
	}

	public void setHemoglobina(Double hemoglobina) {
		this.hemoglobina = hemoglobina;
	}

	public Double getHematocrito() {
		return hematocrito;
	}

	public void setHematocrito(Double hematocrito) {
		this.hematocrito = hematocrito;
	}

	public Double getVcm() {
		return vcm;
	}

	public void setVcm(Double vcm) {
		this.vcm = vcm;
	}

	public Double getHcm() {
		return hcm;
	}

	public void setHcm(Double hcm) {
		this.hcm = hcm;
	}

	public Double getChcm() {
		return chcm;
	}

	public void setChcm(Double chcm) {
		this.chcm = chcm;
	}

	public Double getRdwSd() {
		return rdwSd;
	}

	public void setRdwSd(Double rdwSd) {
		this.rdwSd = rdwSd;
	}

	public Double getRdwCv() {
		return rdwCv;
	}

	public void setRdwCv(Double rdwCv) {
		this.rdwCv = rdwCv;
	}

}
